DROP SEQUENCE SYSTEM.ID_SEQUENCE_SEQ;
CREATE SEQUENCE SYSTEM.ID_SEQUENCE_SEQ
    MINVALUE 1
    MAXVALUE 10000000
    START WITH  1
    INCREMENT BY 20
    CACHE 20;
GRANT SELECT ON SYSTEM.ID_SEQUENCE_SEQ TO scott;
DROP PUBLIC SYNONYM ID_SEQUENCE_SEQ;
CREATE PUBLIC SYNONYM ID_SEQUENCE_SEQ FOR SYSTEM.ID_SEQUENCE_SEQ;

